/**
 * MathJax 配置文件
 * MathJax 是一个用于在网页上显示数学公式的 JavaScript 库
 * 这个文件配置了如何识别和渲染数学公式
 */

// 在全局 window 对象上创建 MathJax 配置
window.MathJax = {
    // TeX 输入处理器的配置
    tex: {
        /**
         * 行内公式的分隔符
         * 行内公式是嵌入在文字中的小公式
         * 例如：文字中的 $x^2$ 会被渲染为 x²
         */
        inlineMath: [
            ['$', '$'],         // 使用单个 $ 符号包裹，如 $E=mc^2$
            ['\\(', '\\)']      // 或使用 \( \) 包裹，如 \(E=mc^2\)
        ],
        
        /**
         * 显示公式的分隔符
         * 显示公式是独立成行、居中显示的大公式
         * 例如：单独一行的 $$x^2$$ 会被渲染为居中的大公式
         */
        displayMath: [
            ['$$', '$$'],       // 使用双 $ 符号包裹
            ['\\[', '\\]']      // 或使用 \[ \] 包裹
        ]
    },
    
    // 启动配置
    startup: {
        /**
         * 页面准备就绪时的回调函数
         * 当 DOM 加载完成后，MathJax 会调用这个函数
         * 返回默认的页面准备处理，确保公式正确渲染
         */
        pageReady: () => {
            return MathJax.startup.defaultPageReady();
        }
    }
};
